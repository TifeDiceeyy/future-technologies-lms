export interface AttendanceRecord {
  id: number;
  studentId: number;
  studentName: string;
  courseId: number;
  courseName: string;
  present: number;
  total: number;
  lastSession: string;
  status: "good" | "warning" | "critical";
}

function deriveStatus(
  present: number,
  total: number,
): "good" | "warning" | "critical" {
  const pct = total === 0 ? 100 : Math.round((present / total) * 100);
  if (pct >= 80) return "good";
  if (pct >= 60) return "warning";
  return "critical";
}

